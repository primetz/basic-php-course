<?php
/*
 * 3. Реализовать основные 4 арифметические операции в виде функций с двумя параметрами.
 *    Обязательно использовать оператор return.
*/
function sum($x, $y) {
    return $x + $y;
}

function diff($x, $y) {
    return $x - $y;
}

function mult($x, $y) {
    return $x * $y;
}

function div($x, $y) {
    return $x / $y;
}
